public class ItemBerita {
    private String idBerita;
    private String sourceBerita;
    private String categoryBerita;
    private String titleBerita;
    private String linkBerita;
    private String pubDateBerita;
    private String imageBerita;

    public String getIdBerita() {
        return idBerita;
    }

    public void setIdBerita(String idBerita) {
        this.idBerita = idBerita;
    }

    public String getSourceBerita() {
        return sourceBerita;
    }

    public void setSourceBerita(String sourceBerita) {
        this.sourceBerita = sourceBerita;
    }

    public String getCategoryBerita() {
        return categoryBerita;
    }

    public void setCategoryBerita(String categoryBerita) {
        this.categoryBerita = categoryBerita;
    }

    public String getTitleBerita() {
        return titleBerita;
    }

    public void setTitleBerita(String titleBerita) {
        this.titleBerita = titleBerita;
    }

    public String getLinkBerita() {
        return linkBerita;
    }

    public void setLinkBerita(String linkBerita) {
        this.linkBerita = linkBerita;
    }

    public String getPubDateBerita() {
        return pubDateBerita;
    }

    public void setPubDateBerita(String pubDateBerita) {
        this.pubDateBerita = pubDateBerita;
    }

    public String getImageBerita() {
        return imageBerita;
    }

    public void setImageBerita(String imageBerita) {
        this.imageBerita = imageBerita;
    }

}
